@app.post("/save_score")
def save_score(data: dict):

    tg_id = str(data["tg_id"])
    score = int(data["score"])

    conn = get_connection()
    cur = conn.cursor()

    cur.execute(
        "SELECT * FROM users WHERE tg_id=?",
        (tg_id,)
    )

    user = cur.fetchone()

    if user:

        best = max(user["best_score"], score)

        cur.execute(
            """
            UPDATE users
            SET score=?, best_score=?
            WHERE tg_id=?
            """,
            (score, best, tg_id)
        )

    else:

        cur.execute(
            """
            INSERT INTO users
            (tg_id, score, best_score)
            VALUES (?, ?, ?)
            """,
            (tg_id, score, score)
        )

    conn.commit()
    conn.close()

    return {"status": "ok"}


