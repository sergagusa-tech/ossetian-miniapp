@app.get("/word")
def get_word():

    word = random.choice(words)
    correct = word["os"]

    wrong = [w["os"] for w in words if w["os"] != correct]

    count = min(3, len(wrong))

    options = random.sample(wrong, count)
    options.append(correct)

    random.shuffle(options)

    return {
        "ru": word["ru"],
        "correct": correct,
        "options": options
    }


