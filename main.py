@app.get("/leaderboard")
def leaderboard():

    conn = get_connection()
    cur = conn.cursor()

    cur.execute(
        """
        SELECT tg_id, best_score
        FROM users
        ORDER BY best_score DESC
        LIMIT 10
        """
    )

    data = cur.fetchall()

    conn.close()

    return [
        {
            "tg_id": row["tg_id"],
            "best_score": row["best_score"]
        }
        for row in data
    ]
